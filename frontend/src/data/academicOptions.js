// اسم المستوى للعرض في الإعدادات وويدجت المكتبة لاحقاً
export function levelLabel(value) {
  const n = Number(value);
  return LEVEL_ORDINALS[n] ? `المستوى ${LEVEL_ORDINALS[n]}` : '';
}
